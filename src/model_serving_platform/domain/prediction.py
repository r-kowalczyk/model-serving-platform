"""Domain transport models for prediction endpoint requests and responses."""

from typing import Literal

from pydantic import BaseModel, Field

AttachmentStrategy = Literal["interaction", "cosine"]


class PredictLinkRequest(BaseModel):
    """Represent request payload for single link prediction.

    This request captures two endpoint entities and optional descriptions
    that can support unseen-entity runtime handling in staged development.
    Parameters: names are required and descriptions are optional.
    """

    entity_a_name: str = Field(min_length=1)
    entity_a_description: str | None = None
    entity_b_name: str = Field(min_length=1)
    entity_b_description: str | None = None
    attachment_strategy: AttachmentStrategy | None = None
    request_id: str | None = None


class PredictLinkResponse(BaseModel):
    """Represent response payload for single link prediction.

    The response includes score, selected strategy, and startup metadata so
    clients can trace which model and bundle generated the prediction.
    Parameters: all values are produced by request orchestration and runtime.
    """

    score: float
    predicted_label: bool | None
    model_version: str
    bundle_version: str | None
    attachment_strategy_used: AttachmentStrategy
    enrichment_status: str
    latency_ms: float
    request_id: str


class PredictionItem(BaseModel):
    """Represent one ranked candidate item in multi-link predictions.

    Keeping this item explicit makes list responses stable and self-describing
    as the API evolves with additional metadata in later stages.
    Parameters: fields identify candidate entity and score.
    """

    entity_name: str
    score: float


class PredictLinksRequest(BaseModel):
    """Represent request payload for ranking links against known candidates.

    This request identifies a source entity and ranking size while allowing
    optional source description for unseen-entity staged runtime behaviour.
    Parameters: top_k controls the number of returned ranked entries.
    """

    entity_name: str = Field(min_length=1)
    entity_description: str | None = None
    top_k: int = Field(default=10, gt=0)
    attachment_strategy: AttachmentStrategy | None = None
    request_id: str | None = None


class PredictLinksResponse(BaseModel):
    """Represent response payload for multi-candidate link predictions.

    The response includes ranked prediction items plus metadata fields that
    mirror single-link responses for consistency across endpoint consumers.
    Parameters: values are generated by service orchestration and runtime.
    """

    predictions: list[PredictionItem]
    predicted_label: bool | None
    model_version: str
    bundle_version: str | None
    attachment_strategy_used: AttachmentStrategy
    enrichment_status: str
    latency_ms: float
    request_id: str
